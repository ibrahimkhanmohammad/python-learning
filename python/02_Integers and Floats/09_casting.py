# Casting = converting one data type into another

'''⚡ CHEAT SHEET

input() → always returns string
int() → cuts decimals
float() → keeps decimals
str() → converts anything to text

int(x)     # → integer
float(x)   # → decimal
str(x)     # → string   '''

# string → int
x = int("10")     # 10

# int → float
y = float(5)      # 5.0

# float → int
z = int(3.9)      # 3 (cuts decimal)

# int → string
a = str(100)      # "100"

